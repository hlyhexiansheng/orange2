package com.orange.entity;

public class Cloth {
	
	private int id;
	
	private int designInfoId;
	
	private int realClothId;
	
	private int size;
	
	private int color;
	
	private String picture;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getDesignInfoId() {
		return designInfoId;
	}

	public void setDesignInfoId(int designInfoId) {
		this.designInfoId = designInfoId;
	}

	public int getRealClothId() {
		return realClothId;
	}

	public void setRealClothId(int realClothId) {
		this.realClothId = realClothId;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public String getPicture() {
		return picture;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}

	
}
