package com.orange.entity;

public class ClothReal {
	
	private int id;
	
	private int size;
	
	private int color;
	
	private int sex;
	
	private int quantity;
	
	private float commomPrice;
	
	private float salePrice;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public float getCommomPrice() {
		return commomPrice;
	}

	public void setCommomPrice(float commomPrice) {
		this.commomPrice = commomPrice;
	}

	public float getSalePrice() {
		return salePrice;
	}

	public void setSalePrice(float salePrice) {
		this.salePrice = salePrice;
	}
	
}
